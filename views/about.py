# ── Methodology ───────────────────────────────────────────────────────────────
st.subheader("🛠 Як ми обробляємо дані")
st.markdown(
    """
Офіційна база даних зберігає тривоги у трьох форматах: по областях, по районах
і по громадах. Щоб порівнювати їх між собою, ми приводимо все до єдиного
«обласного» виміру — ось як:

---

### Крок 1. Які записи беремо?

| Тип тривоги | Беремо? | Чому |
|---|---|---|
| По всій **області** | ✅ Завжди | Офіційний сигнал «вся область під загрозою» |
| По **районах** | ✅ Завжди | З листопада 2025 Україна перейшла на районну систему — без цих записів 2025–2026 роки повністю порожні |
| По **громадах** | ⚠️ Тільки унікальні | 4 з 5 громадських записів — це дублікат обласного/районного сигналу, «продубльованого» вниз. Якщо в цей момент вже є обласна/районна тривога — громадська виключається. Якщо нема — включається |

**Чому «хоча б один район» = тривога по всій області?**
Тривога оголошується для конкретного місця. Якщо в Охтирському районі Сумської
люди йдуть до укриттів — вважаємо, що Сумська «тривожила», навіть якщо
сусідні райони були спокійні. Підхід «всі райони одночасно» пропустив би
більшість цільових ударів.

---

### Крок 2. Склеюємо одночасні тривоги в один епізод

6 районів тривожать паралельно → це **одна** тривога, не шість.
Усі сигнали, що перекриваються або йдуть без паузи, зливаються в один епізод.
Будь-яка перерва — новий окремий запис.

> **Приклад:** о 10:00 тривога в Харківському районі, о 10:30 ще й в Ізюмському
> → **1 епізод**. Якщо о 15:00 нова хвиля після паузи — **2-й** окремий епізод.

**Чому бувають тривоги на 30+ годин?**
У прикордонних областях (Суми, Чернігів) різні райони заходили в тривогу
по черзі без жодної паузи — злиття без перерви дає один довгий епізод.
Це реальна картина багатоденних атак, а не помилка.

---

### Кроки 3–4 (технічні)

- **Час:** переводимо з UTC у київський (з урахуванням літнього/зимового часу)
- **Луганська обл. і АР Крим:** виключені — практично безперервна тривога з
  лютого 2022, яка спотворює будь-яке порівняння з іншими регіонами

---

### Типові запитання

**«Тривог: N» — що рахується?**
Кількість окремих хвиль тривоги (після склеювання), коли хоча б один район
або вся область були під загрозою. Дані коректні як до, так і після переходу
на районну систему у 2025 р.

**Місто Київ і Київська область — це не одне й те саме?**
Ні. Це два незалежні суб'єкти з окремими системами сповіщення, і обидва
присутні в базі як окремі рядки. Збіг часу тривог ~91 % — норма
(місто фізично всередині області).
"""
)

st.divider()
st.subheader("Стан датасету")

with st.spinner("Перевірка датасету…"):
    try:
        df = load_data()
        c1, c2, c3, c4 = st.columns(4)
        c1.metric("Епізодів тривог", f"{len(df):,}")
        _n = df["region"].nunique()
        _rd = f"{_n - 1} + м. Київ" if "м. Київ" in df["region"].values else str(_n)
        c2.metric("Регіонів", _rd)
        c3.metric(
            "Перший запис",
            df["started_at"].min().strftime("%d %b %Y") if not df.empty else "—",
        )
        c4.metric(
            "Останній запис",
            df["started_at"].max().strftime("%d %b %Y") if not df.empty else "—",
        )
    except FileNotFoundError:
        st.warning(
            "Датасет не знайдено. Запустіть `python scripts/download_data.py` "
            "щоб завантажити дані.",
            icon="⚠️",
        )

